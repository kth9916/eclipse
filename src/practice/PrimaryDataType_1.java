package practice;
public class PrimaryDataType_1 {
	public static void main(String[] args) {
		boolean bool1 = true;
		boolean bool2 = false;
		System.out.println(bool1);
		System.out.println(bool2);
		System.out.println();
		
		byte value01 = 10;
		short value02 = -10;
		int value03 = 100;
		long value04 = -100L;
		System.out.println(value01);
		System.out.println(value02);
		System.out.println(value03);
		System.out.println(value04);
		System.out.println();
		
		float value05 = 1.2F;
		double value06 = 1.5;
		double value07 = 5;
		System.out.println(value05);
		System.out.println(value06);
		System.out.println(value07);
	}

}
